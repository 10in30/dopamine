// Heartburst as a Dopamine effect on the Swift backbone — mirror of the web
// `effect-heartburst/src/index.ts`.
//
// Per the generalization mandate, the ONLY per-effect code here is {the MSL
// shader (in Shaders/) + the bespoke tempo (HeartburstTempo.swift) + a tiny
// config naming its scalar params and packing them into the uniform struct}.
// Everything else — the `.dope` mapping, the warm OKLCH palette, the registry,
// the two-pass loop, the standard uniforms, the shadow geometry — is shared
// DopamineCore. The numeric/palette bag comes verbatim from the bundled `.dope`
// (the SAME bytes as the web).
//
// HYBRID (Canvas2D-panel) effect: the web draws the hero heart + the flurry of
// little burst hearts as vector heart curves into ONE offscreen Canvas2D panel
// each frame (heartburst-renderer.ts) and hands it to the shader as a texture.
// That `draw()` is the genuinely code-shaped JS that stays on the host side and
// does NOT port to MSL; this package contributes the shader (which lights that
// panel) + the bespoke beat/burst tempo, exactly the web's per-effect surface.

import Foundation
import DopamineCore

#if canImport(Metal)
import simd
