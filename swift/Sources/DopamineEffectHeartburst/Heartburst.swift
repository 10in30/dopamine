/// Heartburst: resolves a feeling → the flat `.dope` param bag. The drawable side
/// is Metal-only (below).
public final class Heartburst: EffectFactory {
    public let name = "heartburst"
    public let doc: DopeDoc

    public init() throws {
        // Load the bundled `.dope` (the EXACT web bytes) from this package's
        // resource bundle — proving the data spine is shared verbatim.
        self.doc = try DopeResource.loadDope("heartburst.dope", bundle: .module)
    }

    /// Resolve via the shared loader. Heartburst declares NO consts (web
    /// `consts: {}`); `heartburstSeed` is the scatter key — both byte-identical
    /// to the web call.
    public func resolve(_ feeling: DopeResolveInput) throws -> [String: DopeValue] {
        try resolveDopeParams(doc, feeling, consts: [:], scatterKey: "heartburstSeed")
    }
}

