/**
 * gen-uniforms — generate the per-effect uniform STRUCT + packer + name list
 * from the effect's `.dope`, killing the hand-written GLSL→MSL binding seam.
 *
 * WHY THIS EXISTS
 * ---------------
 * WebGL sets uniforms one-by-one by NAME at runtime (`gl.uniform*(u<Name>, …)`),
 * so the web side never declares a struct — it auto-binds each resolved
 * `render.params` scalar to `u<Name>` (see framework/pass-common.ts
 * `computeScalarBinds`). Metal has no per-name uniform setters: a fragment shader
 * reads ONE `constant Uniforms &u [[buffer(0)]]` struct, which forced a
 * hand-written MSL struct + a hand-written Swift packer PER effect — three places
 * (GLSL name list, MSL struct, Swift packer) that silently drift.
 *
 * This script makes that binding map DATA. From the `.dope` `render.params`
 * (names + int/float) plus the runner's standard uniform set, it emits, all from
 * one source of truth:
 *   (a) MSL  — `struct <Name>Uniforms { … }` with the exact Metal field
 *              order/alignment the shader `#include`s.
 *   (b) Swift— the `<Name>Uniforms` struct + `pack<Name>Uniforms(...)` that
 *              fills it from the resolved `[String: DopeValue]` param bag.
 *   (c) Web  — the `u<Name>` uniform-name list (so the GLSL `UNIFORMS` array /
 *              the TS `PassConfig.uniforms` stay in sync with the same data).
 *
 * WHAT THE `.dope` CANNOT TELL US (the per-effect manifest)
 * ---------------------------------------------------------
 * The `.dope` lists `render.params`, but a few binding facts live in the runner
 * contract, not the data:
 *   - which standard uniforms the runner always provides (resolution, origin,
 *     palette, life/time/style/amp, the shadow set);
 *   - the scatter key (a resolved param the shader reads but that is keyed off
 *     the seed, e.g. `moteSeed`);
 *   - params that are NOT uniforms (e.g. `overshoot` feeds the envelope; `style`
 *     and `durationMs` are standard / tempo, not per-effect fields);
 *   - per-frame extras + texture-plumbing fields the `frame()` hook / host fill
 *     (e.g. `check`, `checkBox`, `sdfOn`).
 * Those live in EFFECTS below — one small declarative block per effect. The
 * field set + types + order otherwise come straight from the `.dope`, so adding
 * the other 8 effects is "add a manifest entry + run", not "hand-write a struct".
 *
 * Usage:
 *   node scripts/gen-uniforms.mjs [effect ...]   # default: all known effects
 *   node scripts/gen-uniforms.mjs --check        # generate to temp + diff (CI)
 */
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { readFile, writeFile } from "node:fs/promises";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const GEN_HEADER = "// @generated by scripts/gen-uniforms.mjs — do not edit";

// ---------------------------------------------------------------------------
// The standard uniform set the pass-runner always provides, in the EXACT order
// `StandardUniforms` (Swift) / the MSL struct declare them. This mirrors
// framework/pass-runner.ts `STANDARD` + pass-common `STANDARD_COMMON`. Each
// entry: { name, type, web } where `web` is the GLSL uniform name (for list c).
// ---------------------------------------------------------------------------
const STANDARD = [
  { name: "resolution", type: "float2", web: "uResolution" },
  { name: "origin", type: "float2", web: "uOrigin" },
  { name: "life", type: "float", web: "uLife" },
  { name: "timeS", type: "float", web: "uTimeS" },
  { name: "style", type: "float", web: "uStyle" },
  { name: "amp", type: "float", web: "uAmp" },
  { name: "c0", type: "float3", web: "uC0" },
  { name: "c1", type: "float3", web: "uC1" },
  { name: "c2", type: "float3", web: "uC2" },
  { name: "shadow", type: "float", web: "uShadow" },
  { name: "shadowOffset", type: "float2", web: "uShadowOffset" },
  { name: "shadowSoft", type: "float", web: "uShadowSoft" },
  { name: "shadowStrength", type: "float", web: "uShadowStrength" },
];

// ---------------------------------------------------------------------------
// Per-effect manifest. ONE block per effect; the param FIELDS + types come from
// the `.dope`, this only records the runner-contract facts the data can't carry.
// ---------------------------------------------------------------------------
const EFFECTS = {
  solarbloom: {
    // Where the `.dope` lives + where to write the generated files.
    dope: "swift/Sources/DopamineEffectSolarbloom/Resources/solarbloom.dope.json",
    swiftOut: "swift/Sources/DopamineEffectSolarbloom/SolarbloomUniforms.swift",
    mslOut: "swift/Sources/DopamineEffectSolarbloom/Shaders/SolarbloomUniforms.metal",
    // Web name-list kept OUT of the SwiftPM Sources tree (so it isn't an
    // unhandled resource); it documents the GLSL `u<Name>` superset for the TS.
    webOut: "swift/Generated/solarbloom.uniforms.json",
    // `render.params` that are NOT shader uniforms (web `bindings: null` /
    // standard / tempo). `style` is the standard uStyle; `overshoot` feeds the
    // envelope; `durationMs` is tempo. Excluded from the per-effect struct half.
    excludeParams: ["style", "overshoot", "durationMs"],
    // A resolved param the shader reads, keyed off the seed (web: scatterKey,
    // bound via `bindings: { moteSeed: "uMoteSeed" }`). Appended after the
    // .dope render.params, before the frame/plumbing extras.
    scatterKey: "moteSeed",
    // Per-frame + host/texture-plumbing fields (filled by the config `frame()`
    // hook and the Metal host, not the loader). Order is the struct tail.
    extras: [
      { name: "check", type: "float", web: "uCheck", note: "checkProgress(animMs)" },
      { name: "checkBox", type: "float", web: "uCheckBox", note: "half-size (device px) of glyph box" },
      { name: "checkTexOn", type: "float", web: "uCheckTexOn", note: "1 = sample the font glyph texture" },
      { name: "sdfOn", type: "float", web: "uSdfOn", note: "1 = sample baked SDF" },
      { name: "sdfRangePx", type: "float", web: "uSdfRangePx", note: "" },
      { name: "sdfStrokePx", type: "float", web: "uSdfStrokePx", note: "" },
    ],
    // Sampler uniforms the shader declares (texture-bound, not in the struct);
    // emitted into the web name-list only so list (c) stays a complete superset.
    samplers: ["uCheckTex", "uSdfTex"],
  },
};

// ---------------------------------------------------------------------------
// Type helpers. The generator emits BOTH MSL and Swift from the same field list,
// so the two layouts are consistent BY CONSTRUCTION. The field ORDER below
// (standard → .dope params → scatterKey → extras) is the contract the shader's
// `#include`d struct relies on; Metal aligns float2=8 / float3=16 / float=4 and
// Swift's SIMD2/SIMD3 match those, so grouping is preserved across both.
// ---------------------------------------------------------------------------
const MSL_TYPE = { float: "float", float2: "float2", float3: "float3" };
const SWIFT_TYPE = { float: "Float", float2: "SIMD2<Float>", float3: "SIMD3<Float>" };
const SWIFT_ZERO = { float: "0", float2: "SIMD2<Float>(0, 0)", float3: "SIMD3<Float>(0, 0, 0)" };

/** `bloomRadius` → `uBloomRadius` (the web `u<Name>` convention). */
const cap = (s) => `u${s.charAt(0).toUpperCase()}${s.slice(1)}`;

/** Build the ordered field list for an effect from its `.dope` + manifest. */
function buildFields(dope, m) {
  const fields = [];
  // (1) standard half — copied straight from StandardUniforms.
  for (const s of STANDARD) fields.push({ ...s, kind: "standard" });

  // (2) the effect's own render.params, in authored `.dope` order, skipping the
  // non-uniform ones. `int` and `float` both pack as a single Metal float (the
  // shader reads them as float, exactly like the web `gl.uniform1f`).
  const params = dope.render?.params ?? {};
  for (const [name, spec] of Object.entries(params)) {
    if (m.excludeParams.includes(name)) continue;
    if (name === m.scatterKey) continue; // appended explicitly below
    fields.push({ name, type: "float", web: cap(name), kind: "param", dopeType: spec.type });
  }

  // (3) the scatter key (resolved, seed-keyed; the shader reads it by name).
  if (m.scatterKey) {
    fields.push({ name: m.scatterKey, type: "float", web: cap(m.scatterKey), kind: "scatter" });
  }

  // (4) per-frame + plumbing extras (filled by frame()/host, not the loader).
  for (const e of m.extras) fields.push({ ...e, kind: "extra" });
  return fields;
}

function pascal(name) {
  return name.charAt(0).toUpperCase() + name.slice(1);
}

// ---------------------------------------------------------------------------
// Emitters
// ---------------------------------------------------------------------------
function emitMSL(effect, fields) {
  const Name = pascal(effect);
  const lines = [];
  lines.push(GEN_HEADER);
  lines.push(`// MSL uniform struct for the ${effect} fragment shader. \`#include\` this from`);
  lines.push(`// the \`.metal\` and read \`constant ${Name}Uniforms &u [[buffer(0)]]\`. Field`);
  lines.push(`// ORDER/types match the Swift \`${Name}Uniforms\` packer (gen-uniforms emits both).`);
  lines.push(`#ifndef ${effect.toUpperCase()}_UNIFORMS_H`);
  lines.push(`#define ${effect.toUpperCase()}_UNIFORMS_H`);
  lines.push(`#include <metal_stdlib>`);
  lines.push(`using namespace metal;`);
  lines.push(``);
  lines.push(`struct ${Name}Uniforms {`);
  let lastKind = null;
  const kindLabel = {
    standard: "    // --- standard (StandardUniforms, in declared order) ---",
    param: `    // --- ${effect} render.params (auto-bound by name) ---`,
    scatter: "    // --- scatter key (seed-keyed; read by name) ---",
    extra: "    // --- frame extras + plumbing (filled by frame()/host) ---",
  };
  for (const f of fields) {
    if (f.kind !== lastKind) { lines.push(kindLabel[f.kind]); lastKind = f.kind; }
    const note = f.note ? `   // ${f.note}` : "";
    lines.push(`    ${MSL_TYPE[f.type].padEnd(6)} ${f.name};${note}`);
  }
  lines.push(`};`);
  lines.push(`#endif`);
  lines.push(``);
  return lines.join("\n");
}

function emitSwift(effect, fields) {
  const Name = pascal(effect);
  const lines = [];
  lines.push(GEN_HEADER);
  lines.push(`// Swift uniform struct + packer for ${effect}. Its memory layout MUST match`);
  lines.push(`// \`struct ${Name}Uniforms\` in Shaders/${Name}Uniforms.metal — gen-uniforms`);
  lines.push(`// emits both from the same field list, so they cannot drift.`);
  lines.push(`//`);
  lines.push(`// The GLSL→MSL binding seam, datafied: the web's per-name \`gl.uniform*\` becomes`);
  lines.push(`// this one packed struct, filled from the resolved \`.dope\` param bag by name.`);
  lines.push(`#if canImport(Metal)`);
  lines.push(`import simd`);
  lines.push(`import DopamineCore`);
  lines.push(``);
  lines.push(`public struct ${Name}Uniforms {`);
  let lastKind = null;
  const kindLabel = {
    standard: "    // standard (matches StandardUniforms order)",
    param: `    // ${effect} render.params (auto-bound by name from the resolved bag)`,
    scatter: "    // scatter key (seed-keyed)",
    extra: "    // frame extras + plumbing (filled by frame()/host)",
  };
  for (const f of fields) {
    if (f.kind !== lastKind) { lines.push(kindLabel[f.kind]); lastKind = f.kind; }
    lines.push(`    public var ${f.name}: ${SWIFT_TYPE[f.type]} = ${SWIFT_ZERO[f.type]}`);
  }
  lines.push(`    public init() {}`);
  lines.push(`}`);
  lines.push(``);

  // The packer. Standard half ← StandardUniforms; params ← bag by name; scatter
  // ← bag by name; extras ← the `extras` dictionary (frame()/host filled).
  lines.push(`/// Pack the resolved bag → the struct. The standard half is copied from`);
  lines.push(`/// \`StandardUniforms\`; each named scalar maps to its struct field (the web's`);
  lines.push(`/// \`u<Name>\` auto-bind); \`extras\` carries the frame()/host values.`);
  lines.push(`public func pack${Name}Uniforms(`);
  lines.push(`    standard s: StandardUniforms,`);
  lines.push(`    params: [String: DopeValue],`);
  lines.push(`    extras: [String: Double]`);
  lines.push(`) -> ${Name}Uniforms {`);
  lines.push(`    var u = ${Name}Uniforms()`);
  lines.push(`    // standard half`);
  for (const f of fields.filter((x) => x.kind === "standard")) {
    lines.push(`    u.${f.name} = s.${f.name}`);
  }
  lines.push(`    // resolved scalar params (by name)`);
  lines.push(`    func f(_ k: String) -> Float { if case let .number(v)? = params[k] { return Float(v) }; return 0 }`);
  for (const f of fields.filter((x) => x.kind === "param" || x.kind === "scatter")) {
    lines.push(`    u.${f.name} = f("${f.name}")`);
  }
  lines.push(`    // frame extras + plumbing`);
  for (const f of fields.filter((x) => x.kind === "extra")) {
    lines.push(`    u.${f.name} = Float(extras["${f.name}"] ?? 0)`);
  }
  lines.push(`    return u`);
  lines.push(`}`);
  lines.push(`#endif`);
  lines.push(``);
  return lines.join("\n");
}

function emitWeb(effect, fields, m) {
  // The complete `u<Name>` superset the shader reads: standard + params +
  // scatter + extras + samplers. The web `PassConfig.uniforms` / GLSL `UNIFORMS`
  // array is this list (standard ones are added by the runner, so the per-effect
  // `uniforms:` is the non-standard tail — emitted as `effectUniforms`).
  const standardWeb = STANDARD.map((s) => s.web);
  const effectUniforms = [
    ...fields.filter((x) => x.kind !== "standard").map((x) => x.web),
    ...(m.samplers ?? []),
  ];
  return JSON.stringify(
    {
      _generated: "scripts/gen-uniforms.mjs — do not edit",
      effect,
      // standard uniforms the runner provides (informational; runner adds these).
      standardUniforms: standardWeb,
      // the per-effect `PassConfig.uniforms` tail (matches index.ts `uniforms:`).
      effectUniforms,
      // the full superset the GLSL declares.
      allUniforms: [...standardWeb, ...effectUniforms],
    },
    null,
    2,
  ) + "\n";
}

// ---------------------------------------------------------------------------
// Driver
// ---------------------------------------------------------------------------
async function genEffect(effect, { check }) {
  const m = EFFECTS[effect];
  if (!m) throw new Error(`unknown effect '${effect}' (known: ${Object.keys(EFFECTS).join(", ")})`);
  const dope = JSON.parse(await readFile(join(root, m.dope), "utf8"));
  const fields = buildFields(dope, m);

  const outputs = [
    [m.mslOut, emitMSL(effect, fields)],
    [m.swiftOut, emitSwift(effect, fields)],
    [m.webOut, emitWeb(effect, fields, m)],
  ];

  let stale = false;
  for (const [rel, content] of outputs) {
    const path = join(root, rel);
    let prev = null;
    try { prev = await readFile(path, "utf8"); } catch { /* new file */ }
    if (prev !== content) {
      stale = true;
      if (check) {
        console.error(`STALE: ${rel} (run \`npm run gen-uniforms\`)`);
      } else {
        await writeFile(path, content);
        console.log(`${prev === null ? "created" : "wrote  "} ${rel}`);
      }
    } else if (!check) {
      console.log(`ok     ${rel}`);
    }
  }
  return stale;
}

async function main() {
  const args = process.argv.slice(2);
  const check = args.includes("--check");
  const effects = args.filter((a) => !a.startsWith("--"));
  const targets = effects.length ? effects : Object.keys(EFFECTS);

  let anyStale = false;
  for (const e of targets) anyStale = (await genEffect(e, { check })) || anyStale;

  if (check && anyStale) {
    console.error("\ngen-uniforms: generated files are stale. Run `npm run gen-uniforms` and commit.");
    process.exit(1);
  }
  if (check) console.log("gen-uniforms: all generated files up to date.");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
