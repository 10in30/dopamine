#if canImport(Metal)

// NOTE: `InkstrokeUniforms` + `packInkstrokeUniforms(...)` are now GENERATED by
// scripts/gen-uniforms.mjs into `InkstrokeUniforms.swift` (the SAME source that
// emits the MSL `struct InkstrokeUniforms`), so the Swift struct and the
// `.metal` struct cannot drift. The config below just supplies the standard
// half + the frame()/host-side extras and calls the generated packer.

/// The per-effect pass config. The genuinely code-shaped bits: the MSL function
/// names, the shadow height (= half the stroke length), the per-frame (envelope
/// + stroke draw) hook, and the uniform packer that lays the resolved bag into
/// the struct.
public struct InkstrokeConfig: PassConfig {
    public typealias Uniforms = InkstrokeUniforms
    public var vertexFunction = "inkstroke_vertex"
    public var fragmentFunction = "inkstroke_fragment"
    // The gesture centres on the targeted element (uses origin + target size); it
    // falls back to the canvas centre + full canvas when nothing is targeted.
    public var usesOrigin = true
    public init() {}

    public func shadowHeightFrac(_ params: [String: DopeValue]) -> Double {
        if case let .number(v)? = params["scale"] { return v * 0.5 }
        return 0.35
    }

    public func frame(_ info: FrameInfo, _ params: [String: DopeValue]) -> (amp: Double, extras: [String: Double]) {
        var overshoot = 1.0
        if case let .number(v)? = params["overshoot"] { overshoot = v }
        let amp = envelope(info.life, overshoot: overshoot)
        // The pen draws on its OWN ~360ms clock (bespoke tempo). The web `frame()`
        // feeds the "on twos"-snapped `animMs` to `strokeProgress` (the stroke can
        // share the cel jitter clock), so use `info.animMs` to match parity â€” NOT
        // the un-stepped elapsed time.
        return (amp, ["draw": strokeProgress(info.animMs)])
    }

    /// Pack the resolved bag â†’ the struct via the GENERATED packer
    /// (`packInkstrokeUniforms`). The standard half + named scalars come from the
    /// generated code; here we just hand the frame extras straight through. The
    /// stroke is fully analytic (no baked-SDF / glyph texture), so there are no
    /// host-side texture-plumbing extras to top up (unlike Solarbloom).
    public func packUniforms(standard s: StandardUniforms, params: [String: DopeValue], extras: [String: Double]) -> InkstrokeUniforms {
        return packInkstrokeUniforms(standard: s, params: params, extras: extras)
    }
}
