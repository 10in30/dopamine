/// The single source of truth for the droplet cap: BOTH the MSL `#define
/// MAX_DROPS` and the integer-clamp const the `.dope` references
/// (`clampMax: "MAX_DROPS"`).
public let MAX_DROPS: Double = 64

/// Inkstroke: resolves a feeling â†’ the flat `.dope` param bag. The drawable side
/// is Metal-only (below).
public final class Inkstroke: EffectFactory {
    public let name = "inkstroke"
    public let doc: DopeDoc

    public init() throws {
        // Load the bundled `.dope` (the EXACT web bytes) from this package's
        // resource bundle â€” proving the data spine is shared verbatim.
        self.doc = try DopeResource.loadDope("inkstroke.dope", bundle: .module)
    }

    /// Resolve via the shared loader. `MAX_DROPS` is the only const; `inkSeed`
    /// is the scatter key â€” both byte-identical to the web call.
    public func resolve(_ feeling: DopeResolveInput) throws -> [String: DopeValue] {
        try resolveDopeParams(doc, feeling, consts: ["MAX_DROPS": MAX_DROPS], scatterKey: "inkSeed")
    }
}

