#if canImport(Metal)

// NOTE: `HaloUniforms` + `packHaloUniforms(...)` are GENERATED by
// scripts/gen-uniforms.mjs into `HaloUniforms.swift` (the SAME source that emits
// the MSL `struct HaloUniforms`), so the Swift struct and the `.metal` struct
// cannot drift. The config below just supplies the standard half + the frame()
// breathe gate and calls the generated packer.

/// The per-effect pass config. The genuinely code-shaped bits: the MSL function
/// names, the shadow height (= the ring's outer reach), the per-frame STEADY
/// breathe gate (NOT an envelope), and the uniform packer that lays the resolved
/// bag into the struct.
public struct HaloConfig: PassConfig {
    public typealias Uniforms = HaloUniforms
    public var vertexFunction = "halo_vertex"
    public var fragmentFunction = "halo_fragment"
    public var usesOrigin = true
    public init() {}

    /// A thin floating loop throws a small shadow; key its occluder "height" to
    /// the ring's outer reach (radius + a little width).
    public func shadowHeightFrac(_ params: [String: DopeValue]) -> Double {
        var ringRadius = 0.0, ringWidth = 0.0
        if case let .number(v)? = params["ringRadius"] { ringRadius = v }
        if case let .number(v)? = params["ringWidth"] { ringWidth = v }
        return min(ringRadius + ringWidth * 2, 1)
    }

    public func frame(_ info: FrameInfo, _ params: [String: DopeValue]) -> (amp: Double, extras: [String: Double]) {
        // CONTINUOUS: a STEADY periodic breathe gate driven off elapsed seconds —
        // NOT `envelope(life)`. `animMs/1000` is the seconds clock the shader also
        // reads as `uTimeS`; haloBreathe is periodic with `period`, so the loop seam
        // is exact. `period` is a resolved `.dope` param (the const 1.5).
        var period = HALO_DEFAULT_PERIOD_S
        if case let .number(v)? = params["period"] { period = v }
        let amp = haloBreathe(info.animMs / 1000, period: period)
        // Halo has no per-frame extras (no glyph / texture plumbing); the breathe
        // amp gates global brightness + the shadow geometry.
        return (amp, [:])
    }

    /// Pack the resolved bag → the struct via the GENERATED packer
    /// (`packHaloUniforms`). The standard half + named scalars come from the
    /// generated code; Halo has no host-side extras to top up, so the extras map
    /// passes straight through.
    public func packUniforms(standard s: StandardUniforms, params: [String: DopeValue], extras: [String: Double]) -> HaloUniforms {
        return packHaloUniforms(standard: s, params: params, extras: extras)
    }
}
#endif
