// Halo as a Dopamine effect on the Swift backbone — mirror of the web
// `effect-halo/src/index.ts`.
//
// Per the generalization mandate, the ONLY per-effect code here is {the MSL
// shader (in Shaders/) + a tiny config naming its scalar params and packing them
// into the uniform struct + the per-frame breathe gate}. Everything else — the
// `.dope` mapping, the OKLCH palette, the registry, the two-pass loop, the
// standard uniforms, the shadow geometry — is shared DopamineCore. The
// numeric/palette bag comes verbatim from the bundled `.dope` (the SAME bytes as
// the web).
//
// CONTINUOUS / LOOPING. Halo is Dopamine's first continuous effect. The other
// nine are one-shot reward moments gated by the held-breath `envelope(life)` (a
// 0→peak→0 fade that would not loop). Halo instead drives all motion off PERIODIC
// functions of `uTimeS` in the shader and returns a STEADY periodic `amp` from
// `frame()` (see `haloBreathe`), so it LOOPS SEAMLESSLY: the `.dope` sets `period
// = 1.5 s` and `durationMs = 6000` (= 4 periods), and 1.5 s is exactly 18
// "animate-on-twos" steps, so the frame at `t == durationMs` matches `t == 0` at
// every whimsy.

import Foundation
import DopamineCore

#if canImport(Metal)
import simd
#endif

/// Halo's loop period in seconds. The shader drives every periodic function off
/// this, and the `.dope` makes `durationMs` an integer multiple of it (also a
/// `render.params` uniform `uPeriod`, resolved from the `.dope` const). Mirrored
/// here for the breathe gate so the Swift frame() matches the web exactly.
public let HALO_DEFAULT_PERIOD_S: Double = 1.5

/// Halo's STEADY breathe gate at elapsed seconds `timeS` for loop period
/// `periodS`: a slow sine swinging ~0.7..1.0, periodic in `timeS` (NOT a
/// life-based fade), so the loop is seamless. `haloBreathe(0) == 0.85` and
/// `haloBreathe(N·periodS) == haloBreathe(0)`. Mirror of the web `haloBreathe`
/// (halo-tempo.ts) + the Android `haloBreathe` (HaloTempo.kt).
public func haloBreathe(_ timeS: Double, period periodS: Double) -> Double {
    let ph = (2.0 * Double.pi * timeS) / max(periodS, 1e-3)
    return 0.85 + 0.15 * sin(ph)
}

/// Halo: resolves a feeling → the flat `.dope` param bag. The drawable side is
/// Metal-only (below).
public final class Halo: EffectFactory {
    public let name = "halo"
    public let doc: DopeDoc

    public init() throws {
        // Load the bundled `.dope` (the EXACT web bytes) from this package's
        // resource bundle — proving the data spine is shared verbatim.
        self.doc = try DopeResource.loadDope("halo.dope", bundle: .module)
    }

    /// Resolve via the shared loader. Halo references no clamp consts; `haloSeed`
    /// is the scatter key — both byte-identical to the web call.
    public func resolve(_ feeling: DopeResolveInput) throws -> [String: DopeValue] {
        try resolveDopeParams(doc, feeling, consts: [:], scatterKey: "haloSeed")
    }
}

